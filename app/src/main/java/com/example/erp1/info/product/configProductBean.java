package com.example.erp1.info.product;

public class configProductBean {
    private int id;
    private int matchId;
    private int developingTime;
    private int developingCost;
    private int directCost;
    private String name;
    private int productionCost;
    private int score;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMatchId() {
        return matchId;
    }

    public void setMatchId(int matchId) {
        this.matchId = matchId;
    }

    public int getDevelopingTime() {
        return developingTime;
    }

    public void setDevelopingTime(int developingTime) {
        this.developingTime = developingTime;
    }

    public int getDevelopingCost() {
        return developingCost;
    }

    public void setDevelopingCost(int developingCost) {
        this.developingCost = developingCost;
    }

    public int getDirectCost() {
        return directCost;
    }

    public void setDirectCost(int directCost) {
        this.directCost = directCost;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getProductionCost() {
        return productionCost;
    }

    public void setProductionCost(int productionCost) {
        this.productionCost = productionCost;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
