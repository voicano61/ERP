package com.example.erp1.report;

public class reportBean {
    private balanceBean balance;
    private chargeBean charge;
    private profitBean profit;

    public balanceBean getBalance() {
        return balance;
    }

    public void setBalance(balanceBean balance) {
        this.balance = balance;
    }

    public chargeBean getCharge() {
        return charge;
    }

    public void setCharge(chargeBean charge) {
        this.charge = charge;
    }

    public profitBean getProfit() {
        return profit;
    }

    public void setProfit(profitBean profit) {
        this.profit = profit;
    }
}
